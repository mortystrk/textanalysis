/********
    create column table with TEXT data type column with text analysis and text mining
    only sql console
********/

CREATE COLUMN TABLE blogs(
    ID BIGINT NOT NULL primary key GENERATED BY DEFAULT AS IDENTITY,
    BLOGCONTENT TEXT FAST PREPROCESS OFF SEARCH ONLY OFF ASYNC TEXT ANALYSIS ON TEXT MINING ON
);

CREATE COLUMN TABLE tm_blogs(
    KEY BIGINT NOT NULL primary key GENERATED BY DEFAULT AS IDENTITY,
    ID BIGINT,
    TM_TERM NVARCHAR(5000),
    TM_TERM_TYPE NVARCHAR(100),
    TM_TERM_FREQUENCY INTEGER
);